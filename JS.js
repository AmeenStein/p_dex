function darkmode(){
    document.getElementById("big").style.background = "#333";
    document.getElementById("small").style.background = "#333";
    document.getElementById("very_small").style.background = "#444";
}

function lightmode(){
    document.getElementById("big").style.background = "#fff";
    document.getElementById("small").style.background = "#fff";
    document.getElementById("very_small").style.background = "#333";
}